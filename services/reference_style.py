"""Профиль сценария по референсным роликам."""

from __future__ import annotations

_TARGET_DURATION_SEC = 41.9


def reference_style_prompt_block() -> str:
    return f"""РЕФЕРЕНС (эталонные Shorts — пиши сценарий КАК В НИХ):
- Длина озвучки: ~{_TARGET_DURATION_SEC} сек (макс. 55 сек). НЕ короче 38 сек.
- Тон: уверенный, разговорный — как автор рассказывает ДЕЛО, не рекламу.
- Фразы по 3–6 слов — для субтитров в CapCut.
- Структура: хук → конкретная история/факты → CTA в Telegram.
- Не описывай визуал и монтаж — только текст для озвучки.

СЮЖЕТ (если история или кейс человека):
- Дуга за 45 сек: хук → кто герой → что сделал → как именно → цифра → чем кончилось → CTA.
- После ролика зритель может пересказать историю другу в двух предложениях.
- Каждая фраза двигает сюжет вперёд. Убери фразу мысленно — если ничего не потерялось, это вода.
- Минимум 3 конкретики: имя, возраст/год, компания, сумма, срок.
- Справка из поиска — материал, не приговор. Сценарий пишется всегда.

ХУК (первая фраза):
- Вопрос или ударный факт с конкретикой: сумма, возраст, компания, срок.
- Плохо: «Его схема шокирует», «Как он обманул систему?»
- Хорошо: «Как украли сорок шесть миллионов у Маршалов США?»

ЗАПРЕЩЁННАЯ ВОДА (никогда не пиши):
- «огромную, но пустую», «государству нужен был мост», «быстро, безопасно, эффективно»
- «это стало его фишкой», «неожиданный ход сработал», «теперь это его империя»
- «его схема шокирует» без объяснения схемы в следующих фразах
- абстрактные метафоры без фактов

ПОДАЧА ДЛЯ TTS (стабильная озвучка):
- Восклицательных знаков весь текст: максимум 3–4 (только хук и 1–2 акцента).
- Не ставь «!?» и «?!». Вопрос — «?», удар — «!».
- Имена и бренды пиши просто: «Джон Дагита», без «— вот кто».
- Цифры — словами, не цифрами: «сорок шесть миллионов», не «46 млн».
- Один возраст героя на весь ролик.
- Избегай цепочек из 3+ коротких фраз подряд без факта."""
